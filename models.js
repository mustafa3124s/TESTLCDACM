// البراند المختار
const brand = localStorage.getItem("brand");
const modelsDiv = document.getElementById("models");

// عرض الموديلات
Object.keys(data[brand]).forEach(model => {
    const card = document.createElement("div");
    card.className = "model-card";
    card.innerText = model;
    card.onclick = () => showMatches(model);
    modelsDiv.appendChild(card);
});

// عرض الموديلات المطابقة
function showMatches(model) {
    // العنوان ثابت
    document.getElementById("modal-title").innerText =
        "الموديلات المطابقة";

    const body = document.getElementById("modal-body");
    body.innerHTML = "";

    // الموديل الأساسي (تحت العنوان)
    const mainRow = document.createElement("tr");
    mainRow.innerHTML = `
        <td style="
            font-weight: bold;
            background: #f0f0f0;
            text-align: center;
        ">
            ${model}
        </td>
    `;
    body.appendChild(mainRow);

    // الموديلات المتطابقة
    data[brand][model].forEach(m => {
        const tr = document.createElement("tr");
        tr.innerHTML = `<td>${m}</td>`;
        body.appendChild(tr);
    });

    document.getElementById("modal").style.display = "flex";
}

// إغلاق النافذة
function closeModal() {
    document.getElementById("modal").style.display = "none";
}

// فلترة الموديلات
function filterModels() {
    const value = document.getElementById("search").value.toLowerCase();
    document.querySelectorAll(".model-card").forEach(card => {
        card.style.display =
            card.innerText.toLowerCase().includes(value)
                ? "block"
                : "none";
    });
}

